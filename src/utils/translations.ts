export function getArabicTranslation(text: string): string | undefined {
  if (!text) return undefined;
  
  // Clean text by removing trailing colons, whitespace and common dynamic suffixes
  let clean = text.trim()
      .replace(/:$/, '')
      .replace(/\s*\(Oui\)$/, '')
      .replace(/\s*\(Non\)$/, '')
      .replace(/\s*\(Manuel\)$/, '')
      .replace(/\s*\(Auto\)$/, '')
      .trim();

  // Handle explicit component text containing {t} or similar artifacts
  clean = clean.replace(/\{.*\}/g, '').trim();

  if (arabicTranslations[clean]) return arabicTranslations[clean];
  
  // Match prefix for dynamic labels
  const prefixMatch = Object.keys(arabicTranslations).find(key => clean.startsWith(key));
  if (prefixMatch) return arabicTranslations[prefixMatch];

  return undefined;
}
